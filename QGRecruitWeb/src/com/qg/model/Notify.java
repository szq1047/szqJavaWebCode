package com.qg.model;

public class Notify {
	private int ntfId;
	private String ntfTitle;
	private String ntfContent;
	private String ntfPublisher;
	private String ntfPubDate;
	public int getNtfId() {
		return ntfId;
	}
	public void setNtfId(int ntfId) {
		this.ntfId = ntfId;
	}
	public String getNtfTitle() {
		return ntfTitle;
	}
	public void setNtfTitle(String ntfTitle) {
		this.ntfTitle = ntfTitle;
	}
	public String getNtfContent() {
		return ntfContent;
	}
	public void setNtfContent(String ntfContent) {
		this.ntfContent = ntfContent;
	}
	public String getNtfPublisher() {
		return ntfPublisher;
	}
	public void setNtfPublisher(String ntfPublisher) {
		this.ntfPublisher = ntfPublisher;
	}
	public String getNtfPubDate() {
		return ntfPubDate;
	}
	public void setNtfPubDate(String ntfPubDate) {
		this.ntfPubDate = ntfPubDate;
	}
	
}
