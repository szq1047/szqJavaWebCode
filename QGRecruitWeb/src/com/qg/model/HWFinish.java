package com.qg.model;

public class HWFinish {
	private int userID;
	private String username;
	private String hwTitle;
	private String hwUptime;
	private int hwGroupID;
	private String hwPath;
	private int isCheck;
	private String group;
	public int getUserID() {
		return userID;
	}
	public void setUserID(int userID) {
		this.userID = userID;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getHwTitle() {
		return hwTitle;
	}
	public void setHwTitle(String hwTitle) {
		this.hwTitle = hwTitle;
	}
	public String getHwUptime() {
		return hwUptime;
	}
	public void setHwUptime(String hwUptime) {
		this.hwUptime = hwUptime;
	}
	public int getHwGroupID() {
		return hwGroupID;
	}
	public void setHwGroupID(int hwGroupID) {
		this.hwGroupID = hwGroupID;
	}
	public String getHwPath() {
		return hwPath;
	}
	public void setHwPath(String hwPath) {
		this.hwPath = hwPath;
	}
	public int getIsCheck() {
		return isCheck;
	}
	public void setIsCheck(int isCheck) {
		this.isCheck = isCheck;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
}
