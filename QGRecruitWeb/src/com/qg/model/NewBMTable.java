package com.qg.model;

public class NewBMTable {
	private int tb_ID;
	private String tb_filename;
	private String tb_url;
	private String uptime;
	public int getTb_ID() {
		return tb_ID;
	}
	public void setTb_ID(int tb_ID) {
		this.tb_ID = tb_ID;
	}
	public String getTb_filename() {
		return tb_filename;
	}
	public void setTb_filename(String tb_filename) {
		this.tb_filename = tb_filename;
	}
	public String getTb_url() {
		return tb_url;
	}
	public void setTb_url(String tb_url) {
		this.tb_url = tb_url;
	}
	public String getUptime() {
		return uptime;
	}
	public void setUptime(String uptime) {
		this.uptime = uptime;
	}

}
